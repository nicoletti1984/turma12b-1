package exemplos;

public class Exemplo03 {
    public static void main(String[] args) {
        // TIPO NOME;
        int valor; // cria (declara) uma variável chamada valor, do tipo inteiro
        // warning : aviso: algo "estranho"
        valor = 10; // atribui o valor 10 para a variável 'valor'

        System.out.println(valor);

        valor = 5; // substituição. Uma variável armzena um único valor

        System.out.println(valor);

    }
}
